package java_tutorial.Constants;

public class Constants {

    // To make a constant in the class we write public static final
    public static final int WIDTH  = 800;
    public static final int HEIGTH = 600;


    public static void main(String[] args) {

        // variable that cant be change when a variable its assigned
        // usually static
        // convention =  Uppercase

        System.out.println(HEIGTH+" and "+ Constants.WIDTH);
        // you can called it you by its name
    }
}
