package java_tutorial;

public class if_statements {
    public static void main(String args[]){
        int test =6 ;
        // If / Else test, is how the computer makes decisions/logic.

        if (test != 9){          // inside parenthesis its the conditional , the variable
                                // equal == ,  not equal != , less than < , greater >

            // IF ITS TRUE DO THIS (DOWN BELOW)

            System.out.println("Yes"); // IF ITS TRUE
        }else {                         // IF IS NOT TRUE , OTHERWISE
            System.out.println("No");

        }
    }
}
