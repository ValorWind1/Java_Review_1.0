package java_tutorial;

public class while_loop {

    // Allows us to execute a block of code , multiple times but only write it one time.

    public static void main(String args[]){

     int counter = 0;

     while(counter < 10 ) { // IF TEST CONDITION ITS TRUE THEN , EXECUTE
         System.out.println(counter++);  // EXECUTES THIS LINE OF CODE

     }
    }
}
