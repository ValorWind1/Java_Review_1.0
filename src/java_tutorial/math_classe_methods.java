package java_tutorial;

public class math_classe_methods {

    public static void main(String[] args) {

        System.out.println(Math.abs(-126.7) );  // absolute value

        System.out.println(Math.ceil(7.4));     // rounding numbers up

        System.out.println(Math.floor(7.8)); // rounds down

        System.out.println(Math.max(8.6,5.2 ));   // takes multiple parameters and returns the max from 2 numbers

        System.out.println(Math.min(8.6,5.2)); // takes the lesser of the 2

        System.out.println(Math.pow(5,3)); // 5^3 , five to the power of 3

        System.out.println(Math.sqrt(9));  // square root

    }
}
